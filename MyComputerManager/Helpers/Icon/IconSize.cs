namespace System.Drawing
{
	public enum IconSize : int
	{
		/// <summary>
		/// 32x32 px
		/// </summary>
		Large = 0x0,

		/// <summary>
		/// 16x16 px
		/// </summary>
		Small = 0x1,

		/// <summary>
		/// 48x48 px
		/// </summary>
		ExtraLarge = 0x2,

		/// <summary>
		/// 256x256 px
		/// </summary>
		Jumbo = 0x4
	}
}
